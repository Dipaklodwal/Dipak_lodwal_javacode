package Loopprogramdemo;
public class pattern3 {
    public static void main(String[] args) {
        int r=4;
        int c=5;
        int a=1;
        for(int i=1;i<=r;i++){
            for(int j=1;j<=c;j++){
                System.out.print(a+" ");
                a++;
            }
            System.out.println();
        }
        
    }
    
}
